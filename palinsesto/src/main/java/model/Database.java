package model;

public class Database {
	
	private String datipersonali;
	private String attori;
	private String programmi;
	private String canali;
	
	public String getDatipersonali() {
		return datipersonali;
	}
	public void setDatipersonali(String datipersonali) {
		this.datipersonali = datipersonali;
	}
	public String getAttori() {
		return attori;
	}
	public void setAttori(String attori) {
		this.attori = attori;
	}
	public String getProgrammi() {
		return programmi;
	}
	public void setProgrammi(String programmi) {
		this.programmi = programmi;
	}
	public String getCanali() {
		return canali;
	}
	public void setCanali(String canali) {
		this.canali = canali;
	}

}
