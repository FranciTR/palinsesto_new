package model;

public class Abbonamentistore {

	private String abbonamentocanale;
	private String ondemand;
	private String abbonamentopremium;
	public String getAbbonamentocanale() {
		return abbonamentocanale;
	}
	public void setAbbonamentocanale(String abbonamentocanale) {
		this.abbonamentocanale = abbonamentocanale;
	}
	public String getOndemand() {
		return ondemand;
	}
	public void setOndemand(String ondemand) {
		this.ondemand = ondemand;
	}
	public String getAbbonamentopremium() {
		return abbonamentopremium;
	}
	public void setAbbonamentopremium(String abbonamentopremium) {
		this.abbonamentopremium = abbonamentopremium;
	}
	
}
